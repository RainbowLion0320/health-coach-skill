def get_sodium(food_name, category):
    """Get sodium content for a food."""
    # Check specific overrides first
    if food_name in SPECIFIC_SODIUM:
        return SPECIFIC_SODIUM[food_name]
    # Fall back to category estimate
    return SODIUM_ESTIMATES.get(category, 50)


def fill_defaults(db_path):
    """Fill default values for all foods in database."""
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    # Get all foods
    cursor.execute("SELECT id, name, category FROM custom_foods")
    foods = cursor.fetchall()
    
    updated = 0
    for food_id, name, category in foods:
        # Get values
        sodium = get_sodium(name, category)
        storage = STORAGE_METHODS.get(category, '干货区')
        shelf_life = SHELF_LIFE.get(storage, 30)
        
        # Update record
        cursor.execute("""
            UPDATE custom_foods
            SET sodium_per_100g = ?,
                storage_method = ?,
                default_shelf_life_days = ?,
                updated_at = CURRENT_TIMESTAMP
            WHERE id = ?
        """, (sodium, storage, shelf_life, food_id))
        updated += 1
    
    conn.commit()
    conn.close()
    
    return updated


